#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn all_ascii_printable_have_glyphs() {
        for c in 0x20u8..=0x7E {
            assert!(glyph(c).is_some(), "missing glyph for 0x{c:02X}");
        }
    }

    #[test]
    fn unsupported_chars_return_none() {
        assert!(glyph(0x1F).is_none());
        assert!(glyph(0x7F).is_none());
        assert!(glyph(0xFF).is_none());
    }

    #[test]
    fn space_glyph_is_blank() {
        assert_eq!(space_glyph(), [0; GLYPH_HEIGHT as usize]);
    }

    #[test]
    fn text_width_handles_empty() {
        assert_eq!(text_width(""), 0);
    }

    #[test]
    fn text_width_counts_advances() {
        // "Hi" -> 2 chars * 6 advance - 1 gap = 11
        assert_eq!(text_width("Hi"), 11);
        // "A" -> 6 - 1 = 5
        assert_eq!(text_width("A"), 5);
    }
}
