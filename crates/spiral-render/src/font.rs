//! Built-in 5x7 bitmap font for software rendering.
//!
//! Supports ASCII 0x20 (space) through 0x7E (`~`) — 95 glyphs. Each glyph is
//! stored as 7 rows of 5 bits, least significant bit on the left.
//!
//! This is the only text path in Phase 1. Phase 2 will swap in
//! `swash`/`cosmic-text` for shaped text, but every existing call site
//! continues to use the `DrawText` op unchanged.

/// Width of every glyph bitmap in pixels.
pub const GLYPH_WIDTH: u32 = 5;
/// Height of every glyph bitmap in pixels.
pub const GLYPH_HEIGHT: u32 = 7;
/// Horizontal advance per glyph in pixels (includes 1px gap).
pub const GLYPH_ADVANCE: u32 = 6;

/// Look up a glyph bitmap by ASCII byte. Returns `None` for unsupported chars
/// (callers can substitute a space).
#[must_use]
pub fn glyph(c: u8) -> Option<[u8; GLYPH_HEIGHT as usize]> {
    let idx = match c {
        0x20..=0x7E => (c - 0x20) as usize,
        _ => return None,
    };
    FONT.get(idx).copied()
}

/// Fallback for unsupported characters.
#[must_use]
pub fn space_glyph() -> [u8; GLYPH_HEIGHT as usize] {
    [0; GLYPH_HEIGHT as usize]
}

/// Measure the pixel width of `text` at the default advance.
#[must_use]
pub fn text_width(text: &str) -> u32 {
    if text.is_empty() {
        return 0;
    }
    let chars = text.chars().count() as u32;
    // advance * n - trailing gap
    (chars * GLYPH_ADVANCE).saturating_sub(1)
}

